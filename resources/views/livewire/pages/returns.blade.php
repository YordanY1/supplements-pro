            </li>
            <li>Стоки с нарушена цялост на пломбата, стикери за сигурност или оригинална опаковка;</li>
            <li>Стоки с изтекъл срок на годност, вследствие на забавено връщане от страна на клиента;</li>
            <li>Персонализирани продукти или индивидуално изработени поръчки.</li>
        </ul>

